import { FetchGTASound } from "./FetchGTASound";

// chargeons les fichiers audios.
export const FetchAudios = async () => {
  // le lecteur audio de bruit de clic (gta san andreas)
  const gta_sound = await FetchGTASound(
    require("assets/audios/gta_sa_pick.mp3")
  );

  // tous les lecteurs audios
  return {
    playerGTA: gta_sound,
  };
};
