


/**
 *
 * @param {*} group, un groupe individuel de rows
 * @param {*} rowName, le nom du row déterminant si un row est une pomme pourrie
 * @param {*} rowValue, la valeur du row déterminant si un row est une pomme pourrie
 */
export const deleteRowsInGroup = ({ group, rowName, rowValue }) => {
  return group.filter((elem) => {
    // on veut supprimer les éléments dont rowName === rowValue.
    // filter garde les éléments dont la condition ci dessous est true.
    // donc il nous faut condition qui soit true.
    // donc on veut garder tous les éléments ou
    // rowName est pas égal à rowValue
    return elem[rowName] != rowValue;
  });
};
